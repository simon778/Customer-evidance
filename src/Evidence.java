    private void editjButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editjButton6ActionPerformed
        // TODO add your handling code here:
       try{
           if(First_name_edit_jTextField.getText().equals("")||
                Last_name_edit_jTextField.getText().equals("")||
                contract_Length_edit_TextField4.getText().equals("")||
                amoundInsured_edit_jTextField1.getText().equals("")||
                address_edit_jTextField.getText().equals(""))
            
                throw new EmptyFieldException("Empty fileds!");
           else {
            int rowSelected= jTable1.getSelectedRow();
            int ID= (Integer)(jTable1.getValueAt(rowSelected, 0));
            String insuranceType = (String)InsuranceType_edit_jComboBox1.getSelectedItem();
            Insurance insurnce;
            if(insuranceType==null)
                insurnce= Insurance.UNKNOW;
            else 
                 insurnce= Insurance.valueOf(insuranceType);
            
            String firstName= First_name_edit_jTextField.getText();
            String lastName= Last_name_edit_jTextField.getText();
            int contractDuration= Integer.parseInt(contract_Length_edit_TextField4.getText());
            double amoundInsured= Double.parseDouble(amoundInsured_edit_jTextField1.getText());
            String address = address_edit_jTextField.getText();
          
            
            DateFormat df = new SimpleDateFormat("dd.MM.yyyy");
            Date date = df.parse(dateChooser_edit_Combo1.getText());
            
            Customer customer = new Customer(firstName, lastName, address, date, contractDuration, amoundInsured, insurnce);
           
            e_DBCon.updateCustomer(ID, customer);
            Edit_custommer_jFrame.dispose();
            resetEdit_custommer_jFrame1();
            getCustommers();
             }
       }
        catch (ParseException parseException) {
            System.out.println("Message1" + parseException.getMessage());
            warning_jLabel7.setText(parseException.getMessage());
        }
       catch (EmptyFieldException ex) {
            System.out.println("Message2" + ex.getMessage());
            warning_jLabel7.setText(ex.getMessage());
        }
       catch (NumberFormatException ex) {
            System.out.println("Message2" + ex.getMessage());
            warning_jLabel7.setText("Wrong format. Input number");
        }
       catch (Exception ex) {
            System.out.println("Message" + ex.getMessage());
            warning_jLabel7.setText(ex.getMessage());
        }
       
    }//GEN-LAST:event_editjButton6ActionPerformed

