    private void jTable1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MousePressed
        // TODO add your handling code here:
        edit_jButton2.setEnabled(true);
        delete_jButton1.setEnabled(true);       
        JTable table =(JTable) evt.getSource();
         if (evt.getClickCount() == 2 && table.getSelectedRow() != -1) {
               this.PaymentsJFrame.setSize(800, 600);
               String firstName= (String)table.getValueAt(table.getSelectedRow(), 1);
               String lastName= (String)table.getValueAt(table.getSelectedRow(), 2);
               String ID = "" + table.getValueAt(table.getSelectedRow(), 0);
               first_namejLabel21.setText(firstName);
               last_namejLabel24.setText(lastName);
               ID_payments_jLabel27.setText(ID);
               getPayments();
               this.PaymentsJFrame.setVisible(true);
        }
    }//GEN-LAST:event_jTable1MousePressed

