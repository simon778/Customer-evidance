    private void getPayments(){
        int ID = Integer.parseInt(ID_payments_jLabel27.getText());
        LinkedList<Payment> payments = this.e_DBCon.getPayment(ID);
        Object [] column_name = {"Payment (€)", "Date", "Note"};
        DefaultTableModel model = new DefaultTableModel(){
           
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }
        };
        
        
        model.setColumnIdentifiers(column_name);
        model.setColumnCount(3);
        Object [] row = new Object[3];
        int num_rows =0;
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd.MM.yyyy");
        for (Payment payment : payments){
            String note = payment.getNote();
            Date date = payment.getDate();         
            String d= dateFormat.format(date);
            double paymentAmount  = payment.getPayment();
            row[0] = paymentAmount;
            row[1] = d;         
            row[2] = note;
            num_rows++;
            model.addRow(row);
           
        }
        model.setRowCount(num_rows);   
        jTable2.setModel(model);
    }
