    private void getCustommers(){
            
        Object [] column_name = {"ID","First name", "Last Name", "Address", "Contract date", "Contract lenght", "Amount insured", "Insurance type"};
        DefaultTableModel model = new DefaultTableModel()  {
           
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }
        };
        
        model.setColumnIdentifiers(column_name);
        model.setColumnCount(8);
        
        Object [] row = new Object[8];
    
        LinkedList<Customer> customers = this.e_DBCon.getCustomers();
        int num_rows =0;
        for (Customer customer : customers){
            int ID = customer.getID();
            String firstName = customer.getFirstName();
            String lastName = customer.getLastName();
            String address  = customer.getAddres();
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd.MM.yyyy");
            Date date_of_contract = customer.getContractDate();
            String d = dateFormat.format(date_of_contract);
            int duration_of_contract  = customer.getContractLength();
            double amount_insured  = customer.getAmountInsured();
            String insurence_type  = customer.getInsuranceType().name();
            
            row[0] = ID;
            row[1] = firstName;
            row[2] = lastName;
            row[3] = address;
            row[4] = d;
            row[5] = duration_of_contract;
            row[6] = amount_insured;
            row[7] = insurence_type;
            num_rows++;
            model.addRow(row);
           
        }
        model.setRowCount(num_rows);    
        jTable1.setModel(model);
    }
